CREATE TABLE Transactions (
	transaction_id INT PRIMARY KEY AUTO_INCREMENT,
	Transaction_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	payment_type ENUM('cash', 'credit_card', 'debit_card', 'online_payment', 'cheque', 'other') DEFAULT 'cash',
    unit_price DECIMAL(10, 2) DEFAULT 0.00,
	tax DECIMAL(10, 2) DEFAULT 0.00,
	total DECIMAL(10, 2) DEFAULT 0.00,
	cogs DECIMAL(10, 2) DEFAULT 0.00,
	gross_margin DECIMAL(10, 2) DEFAULT 0.00,
	gross_income DECIMAL(10, 2) DEFAULT 0.00,
	user_id INT,
	store_id INT,
	FOREIGN KEY (user_id) REFERENCES Users(user_id),
	FOREIGN KEY (store_id) REFERENCES Stores(store_id)
);

-- Create a trigger to calculate total amount including tax and update the total column
DELIMITER $$
CREATE TRIGGER calculate_total_amount BEFORE INSERT ON Transactions
FOR EACH ROW
BEGIN
    SET NEW.tax = NEW.unit_price * 0.18;
    SET NEW.total = NEW.unit_price + NEW.tax;
END$$
DELIMITER ;
 
CREATE TABLE Sales (
	sale_id INT PRIMARY KEY AUTO_INCREMENT,
	sale_quantity INT NOT NULL,
	sale_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	product_id INT,
	store_id INT,
	transaction_id INT,
	FOREIGN KEY (product_id) REFERENCES Products(product_id),
	FOREIGN KEY (store_id) REFERENCES Stores(store_id),
	FOREIGN KEY (transaction_id) REFERENCES Transactions(transaction_id)
);
 
CREATE TABLE CustomerOrders (
	order_id INT PRIMARY KEY AUTO_INCREMENT,
	quantity INT NOT NULL,
	order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    expected_delivery_date DATE,
	status ENUM('pending', 'shipped', 'delivered') DEFAULT 'pending',
	product_id INT,
	customer_id INT,
	FOREIGN KEY (product_id) REFERENCES Products(product_id),
	FOREIGN KEY (customer_id) REFERENCES Customers(customer_id)
);

